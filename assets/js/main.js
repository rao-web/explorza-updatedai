/*=============== MODAL ===============*/
document.querySelectorAll(".news-card .button").forEach(btn => {
  btn.addEventListener("click", function (e) {
    e.preventDefault(); // prevent default anchor jump
    const modalId = this.getAttribute("href").substring(1); // remove #
    document.getElementById(modalId).style.opacity = "1";
    document.getElementById(modalId).style.visibility = "visible";
  });
});

// Close modal when clicking the close button
document.querySelectorAll(".modal .close").forEach(closeBtn => {
  closeBtn.addEventListener("click", function (e) {
    e.preventDefault();
    this.closest(".modal").style.opacity = "0";
    this.closest(".modal").style.visibility = "hidden";
  });
});

// Optional: close modal on background click
document.querySelectorAll(".modal").forEach(modal => {
  modal.addEventListener("click", function (e) {
    if (e.target === this) {
      this.style.opacity = "0";
      this.style.visibility = "hidden";
    }
  });
});
